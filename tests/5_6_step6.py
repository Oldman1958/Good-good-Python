"""
Подвиг 4.
На вход программе подается двумерный список размерностью 5 х 5 элементов,
состоящий из нулей и в некоторых позициях единицы (см. пример ниже).
В программе уже реализовано их чтение и сохранение в списке:

s = sys.stdin.readlines()
lst_in = [list(map(int, x.strip().split())) for x in s]

Требуется проверить, не касаются ли единицы друг друга по горизонтали, вертикали и диагонали.
То есть, вокруг каждой единицы должны быть нули. Если проверка проходит вывести на экран "ДА", иначе "НЕТ".
"""

import sys

# считывание списка из входного потока
s = sys.stdin.readlines()
lst_in = [list(map(int, x.strip().split())) for x in s]

# здесь продолжайте программу (используйте список lst_in)

# Размеры матрицы
rows, cols = 5, 5

# Направления для проверки соседей (включая диагонали)
directions = [
    (-1, -1), (-1, 0), (-1, 1),  # верхняя строка
    (0, -1),           (0, 1),   # текущая строка
    (1, -1),  (1, 0),  (1, 1)   # нижняя строка
]

# Флаг для результата проверки
valid = True

# Перебираем все ячейки матрицы
for i in range(rows):
    for j in range(cols):
        # Если нашли единицу
        if lst_in[i][j] == 1:
            # Проверяем всех соседей
            for di, dj in directions:
                ni, nj = i + di, j + dj

                # Проверяем, что сосед находится в пределах матрицы
                if 0 <= ni < rows and 0 <= nj < cols:
                    # Если сосед тоже единица — условие нарушено
                    if lst_in[ni][nj] == 1:
                        valid = False
                        break

            # Если уже нашли нарушение, можно прекратить проверку
            if not valid:
                break

    # Если уже нашли нарушение, прекращаем внешний цикл
    if not valid:
        break

# Выводим результат
print("ДА" if valid else "НЕТ")
